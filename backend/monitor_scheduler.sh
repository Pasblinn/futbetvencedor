    print(f'\nüìà RESULTADOS:')
    print(f'  üü¢ GREENS: {greens}')
    print(f'  üî¥ REDS: {reds}')
    print(f'  ‚è≥ Pendentes: {pending}')
    print(f'  üìä Accuracy: {accuracy:.1f}%')
    print()

finally:
    db.close()
"
}

